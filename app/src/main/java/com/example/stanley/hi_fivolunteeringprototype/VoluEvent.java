package com.example.stanley.hi_fivolunteeringprototype;

public class VoluEvent {

    public String title;
    public String date;
    public String location;
    public String content;
    public Integer picture;
    public VoluEvent(String title, String date, String location, String content, Integer picture) {
        super();
        this.title = title;
        this.date = date;
        this.location = location;
        this.content = content;
        this.picture = picture;
    }
}
